public class Duck {
  private String name, type, behavior;
  
  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type; 
  }

  public String getBehavior() {
    return behavior;
  }
}